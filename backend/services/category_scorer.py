def compute_category_scores(text: str) -> dict[str, int]:
    """
    원본 텍스트와 정규화된 텍스트 양쪽에서 키워드를 탐색해
    카테고리별 0–100점 점수를 반환한다.
    키워드 주변 맥락(부정어·무해 신호)이 있으면 가중치를 감쇄한다.
    """
    normalized = normalize_text(text)
    scores: dict[str, int] = {}

    for cat, keywords in KEYWORDS.items():
        max_w = 0.0
        for kw, weight in keywords:
            kw_norm = kw.lower()
            pos = text.find(kw)
            if pos != -1:
                w = weight * _context_factor(text, kw, pos)
                max_w = max(max_w, w)
            else:
                pos_n = normalized.find(kw_norm)
                if pos_n != -1:
                    w = weight * _context_factor(normalized, kw_norm, pos_n)
                    max_w = max(max_w, w)
        scores[cat] = round(max_w * 100)

    return scores


def compute_calibrated_score(
    model_score: float,
    category_scores: dict[str, int],
) -> float:
    """
    ML 모델 점수와 카테고리 최고점을 가중 조합해 보정된 최종 점수를 산출한다.
    가중치는 환경변수(SCORE_WEIGHT_MODEL / SCORE_WEIGHT_CATEGORY)로 조정 가능하다.
    """
    from config import settings
    w_model = getattr(settings, "SCORE_WEIGHT_MODEL", 0.7)
    w_cat   = getattr(settings, "SCORE_WEIGHT_CATEGORY", 0.3)

    max_cat = max(category_scores.values()) / 100.0 if category_scores else 0.0
    combined = w_model * float(model_score) + w_cat * max_cat
    return round(min(1.0, max(0.0, combined)), 3)
