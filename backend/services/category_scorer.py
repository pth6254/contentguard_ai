# ── 마스킹 우회 정규화 ─────────────────────────────────────────────────────
_OBFUSCATION_CHARS = re.compile(r'[*＊·•■□●◆△▲▷▶◀◁~_\-]+')
_SPACES = re.compile(r'\s+')

# ── 맥락 감쇄: 부정어·무해 신호가 키워드 주변에 있으면 가중치를 낮춤 ─────────
_CONTEXT_WINDOW = 40   # 키워드 전후 탐색 범위(문자 수)
_DAMPEN_FACTOR  = 0.35 # 감쇄 배율 (위험 가중치 × 0.35)

_NEGATION = re.compile(
    r'(?:싶지\s*않|하지\s*마|말아야|안\s*돼|절대\s*안|아니야|못\s+해|'
    r'\bnot\b|\bnever\b|\bdon\'t\b|\bdoesn\'t\b)',
    re.IGNORECASE,
)
_BENIGN = re.compile(
    r'(?:뉴스|보도|기사|연구|소설|영화|드라마|캠페인|피해자|지원|예방|교육|안내|'
    r'신고|상담|경고|조심|주의|분석|통계|학술|논문|'
    r'\bnews\b|\breport\b|\bstudy\b|\bnovel\b|\bmovie\b|\bcampaign\b|'
    r'\bvictim\b|\bsupport\b|\bprevention\b|\beducation\b|\bwarning\b|\bresearch\b|'
    r'\d+\s*[-~]?\s*\d*\s*년|\d+\s*년)',   # 숫자+년 = 기간/연도 맥락 (2-3년, 10년 등)
    re.IGNORECASE,
)


def _context_factor(text: str, kw: str, pos: int) -> float:
    """키워드 주변 ±CONTEXT_WINDOW 문자 안에 부정어나 무해 신호가 있으면 DAMPEN_FACTOR를 반환한다."""
    start = max(0, pos - _CONTEXT_WINDOW)
    end   = min(len(text), pos + len(kw) + _CONTEXT_WINDOW)
    ctx   = text[start:end]
    if _NEGATION.search(ctx) or _BENIGN.search(ctx):
        return _DAMPEN_FACTOR
    return 1.0


def normalize_text(text: str) -> str:
    """
    마스킹 우회 문자를 제거하고 소문자로 변환한다.
    원본 텍스트와 함께 둘 다 검색해 우회 시도를 탐지한다.
    """
    t = _OBFUSCATION_CHARS.sub('', text)
    t = _SPACES.sub(' ', t).strip()
    return t.lower()


